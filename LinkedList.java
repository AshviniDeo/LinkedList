class Node{
	int data;
	@Override
	public String toString() {
		return "Node [data=" + data + ", nextPointer=" + nextPointer + "]";
	}
	Node nextPointer;
	public Node(int data) {
		this.data = data;
		this.nextPointer = null;
	}
}

